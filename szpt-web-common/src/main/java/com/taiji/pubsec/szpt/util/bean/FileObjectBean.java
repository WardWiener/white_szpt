package com.taiji.pubsec.szpt.util.bean;

/**
 * 文件显示Bean
 * 
 * @author huangda
 *
 */
public class FileObjectBean {

	private String id;

	private String name;

	private String size;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getSize() {
		return size;
	}

	public void setSize(String size) {
		this.size = size;
	}

}
