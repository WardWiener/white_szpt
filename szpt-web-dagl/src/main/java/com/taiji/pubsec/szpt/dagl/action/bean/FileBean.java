package com.taiji.pubsec.szpt.dagl.action.bean;

public class FileBean {
	
	/**
	 * 附件id
	 */
	private String id;
	
	/**
	 * 附件name
	 */
	private String name;
	/**
	 * 文件大小
	 */
	private String size;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getSize() {
		return size;
	}

	public void setSize(String size) {
		this.size = size;
	}
	
	

}
