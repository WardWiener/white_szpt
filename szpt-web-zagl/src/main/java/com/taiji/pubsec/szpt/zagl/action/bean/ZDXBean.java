package com.taiji.pubsec.szpt.zagl.action.bean;

public class ZDXBean {
	
	/**
	 * 字典项的id	
	 */
	private String id; 
	
	/**
	 * 字典项的名称
	 */
	private String name;
	/**
	 * 字典项的编码
	 */
	private String code;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	} 
	
	
}
